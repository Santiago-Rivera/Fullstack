// Ejercicio: Tipos de datos en JavaScript

// 1. Crear una variable de tipo String
let nombre = "Juan";
console.log(nombre);

// 2. Crear una variable de tipo Number
let edad = 25;
console.log(edad);

// 3. Crear una variable de tipo Boolean
let esEstudiante = true;
console.log(esEstudiante);

// 4. Crear una variable de tipo Undefined
let noDefinido;
console.log(noDefinido);

// 5. Crear una variable de tipo Null
let vacio = null;
console.log(vacio);

// 6. Crear una variable de tipo Symbol
let idUnico = Symbol("id");
console.log(idUnico);

// 7. Crear una variable de tipo BigInt
let numeroGrande = 1234567890123456789012345678901234567890n;
console.log(numeroGrande);