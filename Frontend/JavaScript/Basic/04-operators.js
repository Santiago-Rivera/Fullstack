// Operadores en JavaScript

// Operadores Aritméticos

// 1. Operador de Suma
let a = 5;
let b = 10;
let suma = a + b;
console.log(suma);

// 2. Operador de Resta
let resta = b - a;
console.log(resta);

// 3. Operador de Multiplicación
let multiplicacion = a * b;
console.log(multiplicacion);

// 4. Operador de División
let division = b / a;
console.log(division);

// 5. Operador de Módulo
let modulo = b % a;
console.log(modulo);

// 6. Operador de Incremento
a++;
console.log(a);

// 7. Operador de Decremento
b--;
console.log(b);

// Operadores de Asignación

a += 2;
console.log(a);

b -= 5;
console.log(b);

a *= 3;
console.log(a);

b /= 2;
console.log(b);

a %= 4;
console.log(a);

b **= 2;
console.log(b);

// Operadores de comparación

console.log(a == b);
console.log(a != b);
console.log(a === b);
console.log(a !== b);
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);

// Operadores lógicos

// AND
console.log(a < b && b > 5);

// OR
console.log(a < b || b < 5);

// NOT
console.log(!(a < b));

// Operadores ternarios

const isRunning = true;

console.log(isRunning ? "El proceso está en ejecución" : "El proceso no está en ejecución");